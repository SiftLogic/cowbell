         }).

%% include
-include_lib("common_test/include/ct.hrl").


%% ===================================================================
%% Callbacks
%% ===================================================================

%% -------------------------------------------------------------------
%% Function: all() -> GroupsAndTestCases | {skip,Reason}
%% GroupsAndTestCases = [{group,GroupName} | TestCase]
%% GroupName = atom()
%% TestCase = atom()
%% Reason = term()
%% -------------------------------------------------------------------
all() ->
    [
     {group, two_nodes}
    ].

%% -------------------------------------------------------------------
%% Function: groups() -> [Group]
%% Group = {GroupName,Properties,GroupsAndTestCases}
%% GroupName = atom()
%% Properties = [parallel | sequence | Shuffle | {RepeatType,N}]
%% GroupsAndTestCases = [Group | {group,GroupName} | TestCase]
%% TestCase = atom()
%% Shuffle = shuffle | {shuffle,{integer(),integer(),integer()}}
%% RepeatType = repeat | repeat_until_all_ok | repeat_until_all_fail |
%%                         repeat_until_any_ok | repeat_until_any_fail
%% N = integer() | forever
%% -------------------------------------------------------------------
groups() ->
    [
     {two_nodes, [shuffle], [
                             two_nodes_connect,
                             two_nodes_reconnect,
                             two_nodes_abandon
                            ]}
    ].
%% -------------------------------------------------------------------
%% Function: init_per_suite(Config0) ->
%%                              Config1 | {skip,Reason} |
%%              {skip_and_save,Reason,Config1}
%% Config0 = Config1 = [tuple()]
%% Reason = term()
%% -------------------------------------------------------------------
init_per_suite(Config) ->
    %% init
    SlaveNodeShortName = cowbell_slave,
    %% start slave
    {ok, SlaveNode} = cowbell_test_suite_helper:start_slave(SlaveNodeShortName),
    %% set environments
    cowbell_test_suite_helper:set_environment_variables(),
    application:set_env(cowbell, nodes, [SlaveNode]),
    %% start app
    cowbell:start(),
    %% config
    [
     {slave_node_short_name, SlaveNodeShortName},
     {slave_node, SlaveNode}
     | Config
    ].

%% -------------------------------------------------------------------
%% Function: end_per_suite(Config0) -> void() | {save_config,Config1}
%% Config0 = Config1 = [tuple()]
%% -------------------------------------------------------------------
end_per_suite(Config) ->
    %% stop app
    cowbell:stop(),
    %% get slave node name
    SlaveNodeShortName = proplists:get_value(slave_node_short_name, Config),
    %% stop slave
    cowbell_test_suite_helper:stop_slave(SlaveNodeShortName).

%% -------------------------------------------------------------------
%% Function: init_per_group(GroupName, Config0) ->
%%                              Config1 | {skip,Reason} |
%%              {skip_and_save,Reason,Config1}
%% GroupName = atom()
%% Config0 = Config1 = [tuple()]
%% Reason = term()
%% -------------------------------------------------------------------
init_per_group(_GroupName, Config) -> Config.

%% -------------------------------------------------------------------
%% Function: end_per_group(GroupName, Config0) ->
%%                              void() | {save_config,Config1}
%% GroupName = atom()
%% Config0 = Config1 = [tuple()]
%% -------------------------------------------------------------------
end_per_group(_GroupName, _Config) -> ok.

%% ----------------------------------------------------------------------------------------------------------
%% Function: init_per_testcase(TestCase, Config0) ->
%%                               Config1 | {skip,Reason} | {skip_and_save,Reason,Config1}
%% TestCase = atom()
%% Config0 = Config1 = [tuple()]
%% Reason = term()
%% ----------------------------------------------------------------------------------------------------------
init_per_testcase(_TestCase, Config) -> Config.

%% ----------------------------------------------------------------------------------------------------------
%% Function: end_per_testcase(TestCase, Config0) ->
%%                               void() | {save_config,Config1} | {fail,Reason}
%% TestCase = atom()
%% Config0 = Config1 = [tuple()]
%% Reason = term()
%% ----------------------------------------------------------------------------------------------------------
end_per_testcase(two_nodes_abandon, Config) ->
    %% get slave node name
    SlaveNodeShortName = proplists:get_value(slave_node_short_name, Config),
    %% stop slave
    cowbell_test_suite_helper:start_slave(SlaveNodeShortName);

end_per_testcase(_TestCase, _Config) -> ok.

%% ===================================================================
%% Tests
%% ===================================================================
two_nodes_connect(Config) ->
    %% get node
    SlaveNode = proplists:get_value(slave_node, Config),

    %% disconnect from slave
    cowbell_test_suite_helper:disconnect_node(SlaveNode),
    timer:sleep(100),

    %% check disconnected
    [] = nodes(),

    %% explicitly connect
    cowbell:connect_nodes(),

    %% check
    [SlaveNode] = nodes().

two_nodes_reconnect(Config) ->
    %% get node
    SlaveNode = proplists:get_value(slave_node, Config),

    %% explicitly connect
    cowbell:connect_nodes(),

    %% disconnect from slave
    cowbell_test_suite_helper:disconnect_node(SlaveNode),
    timer:sleep(100),

    %% check disconnected
    [] = nodes(),

    timer:sleep(2000),

    %% check
    [SlaveNode] = nodes().

two_nodes_abandon(Config) ->
    %% get node
    SlaveNode = proplists:get_value(slave_node, Config),
    SlaveNodeShortName = proplists:get_value(slave_node_short_name, Config),

    %% explicitly connect
    cowbell:connect_nodes(),

    %% check
    [SlaveNode] = nodes(),

    %% stop slave
    cowbell_test_suite_helper:stop_slave(SlaveNodeShortName),
    timer:sleep(100),

    %% check disconnected
    [] = nodes(),

    %% check in disconnected list
    [{SlaveNode, {disconnected, _, _}}] = ets:lookup(cowbell, SlaveNode),

    %% let abandon period go by
    timer:sleep(5000),

    %% check not in disconnected list
    [{SlaveNode, {abandoned, _, _}}] = ets:lookup(cowbell, SlaveNode).
