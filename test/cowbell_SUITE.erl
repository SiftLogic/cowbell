         two_nodes_connect/1,
         two_nodes_reconnect/1,
         two_nodes_abandon/1
        ]).

%% records
-record(state, {
          check_interval_sec = 0 :: non_neg_integer(),
          abandon_node_after_sec :: non_neg_integer(),
